#include "3-calc.h"

/**
 * op_add - function to add two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to modulus two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
